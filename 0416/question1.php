<?php
// 1. 加算結果を表示
$a = 10;
$b = 5;
echo $a + $b . "\n";  // 結果: 15

// 2. 変数 $x = 12 を3倍
$x = 12;
echo $x * 3 . "\n";  // 結果: 36

// 3. 剰余（あまり）
$total = 25;
echo $total % 4 . "\n";  // 結果: 1

// 4. 税込価格（消費税10%）
$price = 1200;
echo $price * 1.1 . "\n";  // 結果: 1320

// 5. 2の5乗
$a = 2;
$b = 5;
echo $a ** $b . "\n";  // 結果: 32
?>